package com.example.projectsas;

public class MindMapElement {
    // top left corner coordinates
    private int x;
    private int y;

    public MindMapElement(int x, int y){
        this.x = x;
        this.y = y;
    }

    public MindMapElement() {
        this.x = 0;
        this.y = 0;
    }
}
